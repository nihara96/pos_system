    public void readData() {
        items = new ArrayList<>();
        try {
            file = new File("bin/data.bin");
            fis = new FileInputStream(file);
            ois = new ObjectInputStream(fis);

            boolean isExist = true;

            while (isExist) {
                if (fis.available() != 0) {
                    Item itm = (Item) ois.readObject();
                    items.add(itm);
                } else {
                    isExist = false;
                }
            }

            for (Item itm : items) {

                System.out.println(itm.getName());

            }

            ois.close();
            fis.close();

        } catch (Exception ex) {
            System.out.println("Daily details File not found ");

        }
    }

    private void readBuisinessData() {
        try {
            file2 = new File("bin/business.bin");
            fis = new FileInputStream(file2);
            ois = new ObjectInputStream(fis);

            boolean isExist = true;

            while (isExist) {
                if (fis.available() != 0) {
                    Buisiness bitm = (Buisiness) ois.readObject();
                    buisinesses.add(bitm);

                } else {
                    isExist = false;
                }
            }
            fis.close();
            ois.close();
        } catch (Exception e) {
            System.out.println("Buisiness File not found ");
        }

    }

