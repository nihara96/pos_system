    private void total() {

        if (!isCalculated) {
            try {
                if (Double.valueOf(txtCash.getText()) > 0) {
                    double discount = 0;
                    double amount = Double.valueOf(txtTotal_amount.getText());
                    double cash = Double.valueOf(txtCash.getText());

                    if (txtDiscount.getText().matches("\\d+")
                            && (Double.valueOf(txtDiscount.getText()) > 0)) {

                        discount = Double.valueOf(txtDiscount.getText());

                        amount = amount - ((amount / 100) * discount);
                        txtTotal_amount.setText(String.valueOf(amount));

                    }

                    double sum = (cash) - amount;

                    txtBalance.setText(String.valueOf(sum));

                    isCalculated = true;

                }

            } catch (NumberFormatException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Valid informations are required to process", "Info" + " ", JOptionPane.INFORMATION_MESSAGE);
            }
        }

    }

